// توليد بيانات الميتا
export async function generateMetadata({ params }: any) {
  const { slug } = await params;
  const decodedSlug = decodeURIComponent(slug);
  const post = posts[decodedSlug as keyof typeof posts];

  if (!post) {
    return {
      title: "المقال غير موجود | اكستريم نانو",
      description: "المقال المطلوب غير متاح حاليًا.",
    };
  }

  return {
    title: `${post.title} | اكستريم نانو`,
    description: post.description,
    keywords: post.keywords.join(", "),
    openGraph: {
      title: `${post.title} | اكستريم نانو`,
      description: post.description,
      type: "article",
      locale: "ar_SA",
      images: [
        {
          url: post.image,
          alt: post.alt || post.title,
        },
      ],
    },
  };
}

